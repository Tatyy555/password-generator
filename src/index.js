let passwordLength = 15;

function generatePassword() {
  let passwordLeft = "";
  let passwordRight = "";

  for (let i = 0; i < passwordLength; i++) {
    randomLeft = Math.floor(Math.random() * characters.length);
    randomRight = Math.floor(Math.random() * characters.length);
    passwordLeft += characters[randomLeft];
    passwordRight += characters[randomRight];
  }

  leftBoxText.textContent = passwordLeft;
  rightBoxText.textContent = passwordRight;
}


